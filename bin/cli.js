const FACTORY_DIRS = [
  ".claude/commands",
  ".claude/agents",
  ".claude/rules",
  ".claude/skills",
  ".claude/hooks",
];

const FACTORY_FILES = [
  ".claude/settings.json",
  ".claude/metrics/SCHEMA.md",
  ".claude/metrics/README.md",
  ".claude/plans/README.md",
];

const PRESERVED_PATHS = [
  ".claude/plans",
  ".claude/metrics",
  ".claude/briefs",
  "CLAUDE.md",
];

const WORKING_DIRS = [".claude/plans", ".claude/metrics", ".claude/briefs"];

const TOP_LEVEL_DIRS = ["templates", "docs"];

// ----------------------------------------------------------------------------
// Version (cached — package.json read at most once per process)
// ----------------------------------------------------------------------------

let _versionCache = null;

function getVersion() {
  if (_versionCache === null) {
    const pkg = JSON.parse(
      fs.readFileSync(path.join(PACKAGE_ROOT, "package.json"), "utf8")
    );
    _versionCache = pkg.version;
  }
  return _versionCache;
}

// ----------------------------------------------------------------------------
// Filesystem helpers
// ----------------------------------------------------------------------------

function copyDirSync(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);
    if (entry.isDirectory()) {
      copyDirSync(srcPath, destPath);
    } else {
      fs.copyFileSync(srcPath, destPath);
    }
  }
}

// Merge `src` into `dest`, leaving any pre-existing files at `dest` untouched.
// Used to restore preserved user files after force-stamp overwrites the pack.
function mergeRestoreDir(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const s = path.join(src, entry.name);
    const d = path.join(dest, entry.name);
    if (entry.isDirectory()) {
      mergeRestoreDir(s, d);
    } else if (!fs.existsSync(d)) {
      fs.copyFileSync(s, d);
    }
  }
}

function existsInTarget(relativePath) {
  return fs.existsSync(path.join(TARGET_DIR, relativePath));
}

// ----------------------------------------------------------------------------
// Pure validation
// ----------------------------------------------------------------------------

function validateInitName(name) {
  if (!name || typeof name !== "string" || name.length === 0) {
    return "name required: usage `prototype-factory init <name>`";
  }
  if (name.startsWith(".") || name.startsWith("-")) {
    return `invalid name "${name}": GitHub repo names cannot start with "." or "-"`;
  }
  if (!/^[A-Za-z0-9._-]+$/.test(name)) {
    return `invalid name "${name}": GitHub repo names must match [A-Za-z0-9._-]+`;
  }
  if (name === CANONICAL_REPO_NAME) {
    return `invalid name "${name}": collides with the canonical pack repo "${CANONICAL_REPO_NAME}"`;
  }
  return null;
}

function plannedInitSteps(workspaceName) {
  return [
    `validate name "${workspaceName.replace(/^cvl-/, "")}" against [A-Za-z0-9._-]+ and canonical-collision rule`,
    `create directory ./${workspaceName}/`,
    `copy factory dirs: ${FACTORY_DIRS.join(", ")}`,
    `copy factory files: ${FACTORY_FILES.join(", ")}`,
    `copy top-level dirs (if present): ${TOP_LEVEL_DIRS.join(", ")}`,
    `create working dirs with .gitkeep: ${WORKING_DIRS.join(", ")}`,
    `write .claude/.factory-version (${getVersion()})`,
    `write .gitignore (SPAWNED_GITIGNORE)`,
    `git init -b main in ./${workspaceName}/`,
    `git add -A && git commit (initial v${getVersion()} stamp)`,
    `(deferred — not run by init) gh repo create / branch protection / git push — happen at developer's discretion`,
  ];
}

// ----------------------------------------------------------------------------
// I/O wrappers
// ----------------------------------------------------------------------------

function ask(question) {
  const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout,
  });
  return new Promise((resolve) => {
    rl.question(question, (answer) => {
      rl.close();
      resolve(answer.trim().toLowerCase());
    });
  });
}

function git(args, cwd) {
  return execFileSync("git", args, {
    stdio: ["ignore", "pipe", "pipe"],
    encoding: "utf8",
    cwd,
  });
}

// ----------------------------------------------------------------------------
// Force-stamp helpers (one concern each)
// ----------------------------------------------------------------------------

async function confirmOverwrite() {
  let existingVersion = "(unknown)";
  const versionFile = path.join(TARGET_DIR, ".claude", ".factory-version");
  if (fs.existsSync(versionFile)) {
    existingVersion = fs.readFileSync(versionFile, "utf8").trim();
  }
  console.log(`Existing factory installation found: ${existingVersion}`);
  const answer = await ask(
    `Overwrite with ${getVersion()}? Plans, metrics, briefs, and CLAUDE.md will be preserved. [y/N] `
  );
  return answer === "y";
}

function preserveUserFiles() {
  const preserved = new Map();
  for (const p of PRESERVED_PATHS) {
    const fullPath = path.join(TARGET_DIR, p);
    if (!fs.existsSync(fullPath)) continue;
    const stat = fs.statSync(fullPath);
    const tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), "cchf-"));
    if (stat.isDirectory()) {
      const tmpCopy = path.join(tmpDir, path.basename(p));
      copyDirSync(fullPath, tmpCopy);
      preserved.set(p, tmpCopy);
    } else {
      const tmpFile = path.join(tmpDir, path.basename(p));
      fs.copyFileSync(fullPath, tmpFile);
      preserved.set(p, tmpFile);
    }
  }
  return preserved;
}

function installFactoryFiles(targetDir) {
  for (const dir of FACTORY_DIRS) {
    const src = path.join(PACKAGE_ROOT, dir);
    const dest = path.join(targetDir, dir);
    if (fs.existsSync(src)) copyDirSync(src, dest);
  }
  for (const file of FACTORY_FILES) {
    const src = path.join(PACKAGE_ROOT, file);
    const dest = path.join(targetDir, file);
    if (fs.existsSync(src)) {
      fs.mkdirSync(path.dirname(dest), { recursive: true });
      fs.copyFileSync(src, dest);
    }
  }
  for (const topLevel of TOP_LEVEL_DIRS) {
    const src = path.join(PACKAGE_ROOT, topLevel);
    if (fs.existsSync(src)) {
      copyDirSync(src, path.join(targetDir, topLevel));
    }
  }
}

function ensureWorkingDirs(targetDir) {
  for (const dir of WORKING_DIRS) {
    const fullDir = path.join(targetDir, dir);
    fs.mkdirSync(fullDir, { recursive: true });
    const gitkeep = path.join(fullDir, ".gitkeep");
    if (!fs.existsSync(gitkeep)) {
      fs.writeFileSync(gitkeep, "");
    }
  }
}

function writeVersionMarker(targetDir) {
  fs.writeFileSync(
    path.join(targetDir, ".claude", ".factory-version"),
    getVersion() + "\n"
  );
}

function restoreUserFiles(preserved) {
  for (const [relativePath, tmpPath] of preserved) {
    const destPath = path.join(TARGET_DIR, relativePath);
    const stat = fs.statSync(tmpPath);
    if (stat.isDirectory()) {
      mergeRestoreDir(tmpPath, destPath);
    } else {
      fs.copyFileSync(tmpPath, destPath);
    }
  }
}

// ----------------------------------------------------------------------------
// init <name> — local-only stamp (no GitHub interaction; deferred-push by design)
// ----------------------------------------------------------------------------

function stampInitWorkspace(workspacePath) {
  fs.mkdirSync(workspacePath, { recursive: false });
  installFactoryFiles(workspacePath);
  ensureWorkingDirs(workspacePath);
  writeVersionMarker(workspacePath);
  fs.writeFileSync(path.join(workspacePath, ".gitignore"), SPAWNED_GITIGNORE);
}

function initialGitCommit(workspacePath) {
  git(["init", "-b", "main"], workspacePath);
  git(["add", "-A"], workspacePath);
  git(
    ["commit", "-m", `Initial v${getVersion()} stamp (prototype factory)`],
    workspacePath
  );
}

function printInitHandoff(workspaceName) {
  console.log(`
============================================================
  Workspace stamped at ./${workspaceName}/
  Local-only — no GitHub repo created, no push.

  When you decide the demo is worth keeping for real:
    cd ${workspaceName}
    gh repo create ${CVL_ORG}/${workspaceName} --private --source . --push

  Or hand it off to the production factory via /elevate-to-brief.

  Next:
    cd ${workspaceName}
    claude /spark
============================================================
`);
}

async function runInit(name, options = {}) {
  const { dryRun = false } = options;
  const validationError = validateInitName(name);
  if (validationError) {
    console.error(`error: ${validationError}`);
    process.exit(1);
  }

  const workspaceName = `cvl-${name}`;
  const workspacePath = path.join(TARGET_DIR, workspaceName);

  if (fs.existsSync(workspacePath)) {
    console.error(
      `error: directory ./${workspaceName}/ already exists; refusing to overwrite`
    );
    console.error(
      `recovery: choose a different name OR remove the existing directory and re-run`
    );
    process.exit(1);
  }

  console.log(
    `\nClaude Code Prototype Factory ${getVersion()} — init ${workspaceName}\n`
  );

  if (dryRun) {
    console.log("Dry-run: planned actions (none executed):");
    for (const step of plannedInitSteps(workspaceName)) {
      console.log(`  - ${step}`);
    }
    console.log("\nNo files written. No external calls made. Exiting.\n");
    return;
  }

  try {
    console.log(`Stamping workspace at ./${workspaceName}/ ...`);
    stampInitWorkspace(workspacePath);
  } catch (err) {
    console.error(`workspace stamping failed:`);
    console.error(err.stack || err.message);
    console.error(
      `recovery: partial workspace at ./${workspaceName}/ (delete manually); then re-run init`
    );
    process.exit(1);
  }

  try {
    console.log(`Initialising local git, making initial commit ...`);
    initialGitCommit(workspacePath);
  } catch (err) {
    console.error(`git step failed:`);
    console.error(err.stderr || err.message);
    console.error(
      `recovery: workspace stamped at ./${workspaceName}/ but git init / commit failed; complete manually or delete and re-run`
    );
    process.exit(1);
  }

  printInitHandoff(workspaceName);
}

// ----------------------------------------------------------------------------
// Default invocation (force-stamp into current dir)
// ----------------------------------------------------------------------------

async function runForceStamp({ force = false } = {}) {
  console.log(`\nClaude Code Prototype Factory ${getVersion()}\n`);

  if (existsInTarget(".claude") && !force) {
    const confirmed = await confirmOverwrite();
    if (!confirmed) {
      console.log("Aborted.");
      process.exit(0);
    }
  }

  const preserved = preserveUserFiles();
  console.log("Installing factory files...");
  installFactoryFiles(TARGET_DIR);
  ensureWorkingDirs(TARGET_DIR);
  writeVersionMarker(TARGET_DIR);
  restoreUserFiles(preserved);

  console.log(
    `\nClaude Code Prototype Factory ${getVersion()} installed successfully.\n`
  );
  console.log("Next steps:");
  console.log("  claude /spark  — start a new demo idea\n");
  console.log("See install.md for the full guide.");
}

// ----------------------------------------------------------------------------
// Entry point — argument dispatch only
// ----------------------------------------------------------------------------

function checkNodeVersion() {
  const nodeMajor = parseInt(process.versions.node.split(".")[0], 10);
  if (nodeMajor < 22) {
    console.error(
      `error: Node 22+ required (current: ${process.versions.node}). Install via nvm / asdf / volta or your system package manager.`
    );
    process.exit(1);
  }
}

function parseArgs(argv) {
  const args = argv.slice(2);
  if (args[0] === "init") {
    const remaining = args.slice(1);
    return {
      command: "init",
      name: remaining.find((a) => !a.startsWith("--")),
      dryRun: remaining.includes("--dry-run"),
    };
  }
  return { command: "force-stamp", force: args.includes("--force") };
}

async function main() {
  checkNodeVersion();
  const opts = parseArgs(process.argv);

  if (opts.command === "init") {
    return runInit(opts.name, { dryRun: opts.dryRun });
  }
  return runForceStamp({ force: opts.force });
}

if (require.main === module) {
  main().catch((err) => {
    console.error("Error:", err.message);
    process.exit(1);
  });
}

